<?php

namespace App\Repository;

use App\Entity\Product;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\ORM\QueryBuilder;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Product>
 */
class ProductRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Product::class);
    }

    public function getQueryOrder(string $order, string $sort): QueryBuilder
    {
        $alias = 'p';

        return $this->createQueryBuilder($alias)->orderBy($alias . '.' . $order, $sort);
    }

    public function getGroupedData()
    {
        $query = $this->createQueryBuilder('p')
            ->select('p.code, p.type, COUNT(p.id) as count, SUM(p.price) as total_price')
            ->groupBy('p.code, p.type')
            ->orderBy('count', 'DESC');

        return $query->getQuery()->getResult();
    }

